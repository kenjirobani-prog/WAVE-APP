平日の朝イチ（日の出〜8時頃）が最もおすすめです。人が少なく、オフショアが吹きやすく、波が整っていることが多いです。週末はどのポイントも混雑するため、初心者には難しい状況になりやすいです。もし週末に行く場合は、早朝に行くか、比較的空いているポイントを選ぶようにしましょう。

